function getEmoji(topic: string): string {
  const emojiMap: Record<string, string> = {
    kinematics: "ЁЯПГтАНтЩВя╕П",
    forces: "ЁЯТк",
    energy: "тЪб",
    waves: "ЁЯМК",
    electricity: "ЁЯТб",
    magnetism: "ЁЯз▓",
    thermal: "ЁЯФе",
    pressure: "ЁЯОИ",
    density: "тЪЦя╕П"
  }
  const key = topic.toLowerCase()
  for (const [k, v] of Object.entries(emojiMap)) {
    if (key.includes(k)) return v
  }
  return "ЁЯФм"
}

