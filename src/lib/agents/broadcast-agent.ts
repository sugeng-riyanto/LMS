export async function generateBroadcast(input: AgentInput): Promise<BroadcastMessage> {
  const deadline = `Deadline: Friday, 11:59 PM (end of week)`

  return {
    wa_message: getWaBody(input.topic, input.grade, input.week_number),
    lms_post: {
      title: getLmsTitle(input.topic, input.week_number),
      body: getLmsBody(input.topic, input.grade),
      attachments: [
        { name: `Worksheet_${input.topic.replace(/\s+/g, "_")}_G${input.grade}_W${input.week_number}.pdf`, url: "#" },
        { name: `CER_Template_G${input.grade}.pdf`, url: "#" },
        { name: `Lab_Safety_Sheet.pdf`, url: "#" }
      ],
      deadline
    },
    parent_message: getParentMessage(input.topic, input.grade)
  }
}
